package org.example;

public class Operators {
    public static void main(String[] args) {
        System.out.println("Java operators");

        // compare operators
        // equals operator ==
        // not equal !=
        int a = 5;
        int b = 6;
        boolean isAEqualToB = a == b;
        System.out.println("Is a equals to b?: " + isAEqualToB);
    }
}









